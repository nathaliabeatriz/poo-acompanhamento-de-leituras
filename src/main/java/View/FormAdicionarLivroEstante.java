    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        Estante estante = new Estante();
        estante.setIdEstante(Integer.parseInt(txtCodigo.getText()));
        int verif = estanteController.excluir(estante);
        if(verif == 1){
            novo();
        }
        else{
            JOptionPane.showMessageDialog(null, "Erro ao excluir os dados!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        Estante estante = new Estante();
        estante.setIdEstante(Integer.parseInt(txtCodigo.getText()));
        estante.setStatus(String.valueOf(cmbStatus.getSelectedItem()));
        if(!lstLeitores.isEmpty()){
            int index = cmbLeitor.getSelectedIndex();
            Leitor leitor = lstLeitores.get(index);
            estante.setLeitor(leitor);
        }
        if(!lstLivrosEstante.isEmpty()){
            int index = cmbLivroEstante.getSelectedIndex();
            Livro livro = lstLivrosEstante.get(index);
            estante.setLivro(livro);
        }
        estanteController.editar(estante);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Estante estante = new Estante();
        estante.setStatus(String.valueOf(cmbStatus.getSelectedItem()));
        if(!lstLeitores.isEmpty()){
            int index = cmbLeitor.getSelectedIndex();
            Leitor leitor = lstLeitores.get(index);
            estante.setLeitor(leitor);
        }
        if(!lstLivros.isEmpty()){
            int index = cmbLivros.getSelectedIndex();
            Livro livro = lstLivros.get(index);
            estante.setLivro(livro);
        }
        estanteController.salvar(estante);
        int codigo = estante.getIdEstante();
        if(codigo != -1){
            txtCodigo.setText(String.valueOf(estante.getIdEstante()));
            JOptionPane.showMessageDialog(null, "Dados salvos com sucesso!");
        } else{
            JOptionPane.showMessageDialog(null, "Erro ao salvar os dados");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

