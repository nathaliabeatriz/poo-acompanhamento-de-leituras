    private void preencherComboLeitor(){
        String nome = txtConsLeitores.getText();
        if(!nome.isEmpty()){
            lstLeitores = leitorController.getLeitores(nome);
            cmbLeitor.removeAllItems();
            lstLeitores.forEach(itemLeitor -> {
                cmbLeitor.addItem(itemLeitor.getNome() + " | " + itemLeitor.getId());
            });
        } else{
            cmbLeitor.removeAllItems();
        }
    }
    
    private void preencherComboLivroEstante(){
        int index = cmbLeitor.getSelectedIndex();
        Leitor leitor = lstLeitores.get(index);
        String nome = txtConsLivroEstante.getText();
        if(!nome.isEmpty()){
            lstLivrosEstante = livroController.getLivrosEstante(nome, leitor);
            cmbLivroEstante.removeAllItems();
            lstLivrosEstante.forEach(itemLivro -> {
                cmbLivroEstante.addItem(itemLivro.getNome() + " | " + itemLivro.getIdLivro());
            });
        } else{
            cmbLivroEstante.removeAllItems();
        }
    }
    
    private void preencherComboForaEstante(){
        int index = cmbLeitor.getSelectedIndex();
        Leitor leitor = lstLeitores.get(index);
        String nome = txtConsLivro.getText();
        if(!nome.isEmpty()){
            lstLivros = livroController.getLivrosForaEstante(nome, leitor);
            cmbLivros.removeAllItems();
            lstLivros.forEach(itemLivro -> {
                cmbLivros.addItem(itemLivro.getNome() + " | " + itemLivro.getIdLivro());
            });
        } else{
            cmbLivros.removeAllItems();
        }
    }
    
    public void preencherCampos(){
        int indexLeitor = cmbLeitor.getSelectedIndex();
        Leitor leitor = lstLeitores.get(indexLeitor);
        int indexLivro = cmbLivroEstante.getSelectedIndex();
        Livro livro = lstLivrosEstante.get(indexLivro);
        Estante estante = estanteController.getEstantes(leitor, livro);
        
        txtCodigo.setText(String.valueOf(estante.getIdEstante()));
        txtConsLivro.setText(estante.getLivro().getNome());
        cmbStatus.setSelectedItem(estante.getStatus());
        btnSalvar.setEnabled(false);
    }
    
