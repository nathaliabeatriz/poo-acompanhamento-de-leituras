    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        Editora editora = new Editora();
        editora.setIdEditora(Integer.parseInt(txtCodigo.getText()));
        editora.setNome(txtNome.getText());
        editora.setSede(txtSede.getText());

        editoraController.editar(editora);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        Editora editora = new Editora();
        editora.setIdEditora(Integer.parseInt(txtCodigo.getText()));
        int verif = editoraController.excluir(editora);
        if(verif == 1){
            novo();
        }
        else{
            JOptionPane.showMessageDialog(null, "Erro ao excluir os dados!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

