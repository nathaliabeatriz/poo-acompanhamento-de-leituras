    private void preencherComboLeitores(){
        String nome = txtConsLeitor.getText();
        if(!nome.isEmpty()){
            lstLeitores = leitorController.getLeitores(nome);
            cmbLeitores.removeAllItems();
            lstLeitores.forEach(itemLeitor -> {
                cmbLeitores.addItem(itemLeitor.getNome() + " | " + itemLeitor.getId());
            });
        } else{
            cmbLeitores.removeAllItems();
        }
    }
    
    private void preencherComboLivros(){
        int index = cmbLeitores.getSelectedIndex();
        Leitor leitor = lstLeitores.get(index);
        String nome = txtConsLivro.getText();
        if(!nome.isEmpty()){
            lstLivros = livroController.getLivrosEstante(nome, leitor);
            cmbLivros.removeAllItems();
            lstLivros.forEach(itemLivro -> {
                cmbLivros.addItem(itemLivro.getNome() + " | " + itemLivro.getIdLivro());
            });
        } else{
            cmbLivros.removeAllItems();
        }
    }
    
        public void preencherCampos(){
            if(!lstAvaliacoes.isEmpty()){
                int index = cmbAvaliacoes.getSelectedIndex();
                Avaliacao avaliacao = lstAvaliacoes.get(index);
                txtCodigo.setText(String.valueOf(avaliacao.getIdAvaliacao()));
                txtTitulo.setText(avaliacao.getTitulo());
                txtEstrelas.setText(String.valueOf(avaliacao.getEstrelas()));
                txtResenha.setText(avaliacao.getResenha());
                btnSalvar.setEnabled(false);

            }
        }
        private void preencherComboAvaliacoes(){
            String titulo = txtConsAvaliacao.getText();
            if(!titulo.isEmpty()){
                lstAvaliacoes = avaliacaoController.getAvaliacoes();
                cmbAvaliacoes.removeAllItems();
                lstAvaliacoes.forEach(itemAvaliacao -> {
                    cmbAvaliacoes.addItem(itemAvaliacao.getTitulo() + " | " + itemAvaliacao.getIdAvaliacao());
                });
            } else{
                cmbAvaliacoes.removeAllItems();
            }
        }
    
    
