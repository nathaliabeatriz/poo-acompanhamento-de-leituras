    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Avaliacao avaliacao = new Avaliacao();
        Livro livro = new Livro();
        avaliacao.setTitulo(txtTitulo.getText());
        avaliacao.setEstrelas(Integer.parseInt(txtEstrelas.getText()));
        avaliacao.setResenha(txtResenha.getText());
        if(!lstLeitores.isEmpty() && !lstLivros.isEmpty()){
            int indexLeitor = cmbLeitores.getSelectedIndex();
            Leitor leitor = lstLeitores.get(indexLeitor);
            int indexLivro = cmbLivros.getSelectedIndex();
            livro = lstLivros.get(indexLivro);
            Estante estante = estanteController.getEstantes(leitor, livro);
            avaliacao.setEstante(estante);
        }

        avaliacaoController.salvar(avaliacao);
        int codigo = avaliacao.getIdAvaliacao();
        if(codigo != -1){
            txtCodigo.setText(String.valueOf(avaliacao.getIdAvaliacao()));
            JOptionPane.showMessageDialog(null, "Dados salvos com sucesso!");
        } else{
            JOptionPane.showMessageDialog(null, "Erro ao salvar os dados");
        }
        
        ObserverMediaAvaliacao observer = new ObserverMediaAvaliacao();
        observer.atualizarMedia(avaliacao.getEstrelas(), livro);
    }//GEN-LAST:event_btnSalvarActionPerformed

