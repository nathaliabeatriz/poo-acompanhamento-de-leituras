    private void preencherCombo(){
        String nome = txtConsNome.getText();
        if(!nome.isEmpty()){
            lstLeitores = leitorController.getLeitores(nome);
            cmbLeitores.removeAllItems();
            lstLeitores.forEach(itemLeitor -> {
                cmbLeitores.addItem(itemLeitor.getNome() + " | " + itemLeitor.getId());
            });
        } else{
            cmbLeitores.removeAllItems();
        }
    }
    
    public void preencherCampos(){
        if(!lstLeitores.isEmpty()){
            int index = cmbLeitores.getSelectedIndex();
            Leitor leitor = lstLeitores.get(index);
            txtCodigo.setText(String.valueOf(leitor.getId()));
            txtNome.setText(leitor.getNome());
            txtDataNascimento.setText(leitor.getDataNascimento());
            txtNacionalidade.setText(leitor.getNacionalidade());
            txtSexo.setText(leitor.getSexo());
            txtEmail.setText(leitor.getEmail());
            btnSalvar.setEnabled(false);
        }
    }
    
