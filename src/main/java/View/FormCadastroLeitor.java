    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Leitor leitor = new Leitor();
        leitor.setNome(txtNome.getText());
        leitor.setDataNascimento(txtDataNascimento.getText());
        leitor.setNacionalidade(txtNacionalidade.getText());
        leitor.setSexo(txtSexo.getText());
        leitor.setEmail(txtEmail.getText());
        
        leitorController.salvar(leitor);
        int codigo = leitor.getId();
        if(codigo != -1){
            txtCodigo.setText(String.valueOf(leitor.getId()));
            JOptionPane.showMessageDialog(null, "Dados salvos com sucesso!");
        } else{
            JOptionPane.showMessageDialog(null, "Erro ao salvar os dados");
        }        // TODO add your handling
    }//GEN-LAST:event_btnSalvarActionPerformed

